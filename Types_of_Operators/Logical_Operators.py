# Logical Operators in Python
# AND Operator
x = True
y = False
and_result = x and y    
print("AND Operator (x and y):", and_result)  # Output: False
# OR Operator
or_result = x or y
print("OR Operator (x or y):", or_result)  # Output: True
# NOT Operator
not_result = not x
print("NOT Operator (not x):", not_result)  # Output: False
