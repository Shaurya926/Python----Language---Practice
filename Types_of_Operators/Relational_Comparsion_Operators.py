# Relational Comparison Operators (==, !=, >, <, >=, <=) in Python
# Equality
x = 10
y = 5
eq_result = (x == y)
print("Equality (x == y):", eq_result)  # Output: False
# Inequality
neq_result = (x != y)
print("Inequality (x != y):", neq_result)  # Output: True
# Greater Than
gt_result = (x > y)
print("Greater Than (x > y):", gt_result)  # Output: True
# Less Than
lt_result = (x < y)
print("Less Than (x < y):", lt_result)  # Output: False
# Greater Than or Equal To
gte_result = (x >= y)
print("Greater Than or Equal To (x >= y):", gte_result)  # Output: True
# Less Than or Equal To
lte_result = (x <= y)
print("Less Than or Equal To (x <= y):", lte_result)  # Output: False
# Comparing equal values
a = 7
b = 7
equal_values_result = (a == b)
print("Comparing equal values (a == b):", equal_values_result)  # Output
# Comparing different values
different_values_result = (a != b)
print("Comparing different values (a != b):", different_values_result)  # Output: False: True
# Comparing greater than or equal with equal values
gte_equal_result = (a >= b)
print("Comparing greater than or equal with equal values (a >= b):", gte_equal_result)  # Output: True
# Comparing less than with different values
lt_different_result = (a < b)   
print("Comparing less than with different values (a < b):", lt_different_result)  # Output: False
