# Application of the fifth data type: List
# None data type
value = None
print("Value:", value)
# Demonstrating None type usage
if value is None:
    print("The value is None")
else:
    print("The value is not None")
print("Type of value:", type(value))
# Assigning a new value
value = 42
print("New Value:", value)
print("Type of new value:", type(value))
