# Application of the fourth data type: List
# Booleans data type
is_sunny = True
is_raining = False
print("Is it sunny?", is_sunny)
print("Is it raining?", is_raining)
# Demonstrating boolean operations
print("NOT is_sunny:", not is_sunny)
print("is_sunny AND is_raining:", is_sunny and is_raining)
print("is_sunny OR is_raining:", is_sunny or is_raining)    
print("is_sunny XOR is_raining:", is_sunny != is_raining)
